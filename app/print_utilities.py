def print_red(chars):
    return '\033[31m' + chars + '\033[0m'


def print_orange(chars):
    return '\033[33m' + chars + '\033[0m'


def print_yellow(chars):
    return '\033[36m' + chars + '\033[0m'


def print_green(chars):
    return '\033[32m' + chars + '\033[0m'


def print_blue(chars):
    return '\033[34m' + chars + '\033[0m'


def print_violet(chars):
    return '\033[35m' + chars + '\033[0m'