def print_red(s):
    return '\033[31m' + s + '\033[0m'

def print_yellow(s):
    return '\033[33m' + s + '\033[0m'

def print_green(s):
    return '\033[32m' + s + '\033[0m'

def print_blue(s):
    return '\033[36m' + s + '\033[0m'

def print_indigo(s):
    return '\033[34m' + s + '\033[0m'

def print_violet(s):
    return '\033[35m' + s + '\033[0m'